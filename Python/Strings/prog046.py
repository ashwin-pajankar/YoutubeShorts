# List - ordered and changeable, duplicate allowed
a = [ "apple", "banana", "cherry"]

# Tuple - ordered and unchangeable, duplicate allowed
a = ( "apple", "banana", "cherry")

# Set - unordered, unchangeable, and unindexed, duplicate not allowed
a = { "apple", "banana", "cherry"}

# Dictionary ordered and changeable, duplicate not allowed
a = { 0:"apple", 1:"banana", 2:"cherry"}